# Some useful facts:
#   1 is not a prime.
#   All primes except 2 are odd.
#   All primes greater than 3 can be written in the form  6k+/-1.
#   Any number n can have only one primefactor greater than sqrt(n).
#   The consequence for primality testing of a number n is:
#       if we cannot find a numberf less than or equal sqrt(n) that divides n
#       then n is prime: the only primefactor of n is n itself
